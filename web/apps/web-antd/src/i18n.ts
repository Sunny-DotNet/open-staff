let initialized = false;

export async function setupAppI18n(app: App) {
  if (initialized) {
    return;
  }

  initialized = true;

  await setupI18n(app, {
    defaultLocale: preferences.app.locale,
    loadMessages: async (locale) =>
      (messages[locale as AppLocale] ?? {}) as unknown as Record<string, string>,
  });

  watch(
    () => i18n.global.locale.value,
    (value) => {
      dayjs.locale(value === 'zh-CN' ? 'zh-cn' : 'en');
      document.documentElement.lang = value;
    },
    { immediate: true },
  );
}

export function t(
  key: string,
  params?: Record<string, number | string>,
): string {
  return i18n.global.t(key, params ?? {}) as string;
}

export function getModuleTitle(moduleKey: string, fallback?: string) {
  const key = getModuleTitleKey(moduleKey);
  const translated = t(key);
  return translated === key ? (fallback ?? moduleKey) : translated;
}

export function getSectionTitle(sectionKey: string, fallback?: string) {
  const key = getSectionTitleKey(sectionKey);
  const translated = t(key);
  return translated === key ? (fallback ?? sectionKey) : translated;
}

export function getModuleTitleKey(moduleKey: string) {
  return moduleTitleKeys[moduleKey] ?? `modules.${moduleKey}`;
}

export function getSectionTitleKey(sectionKey: string) {
  return sectionTitleKeys[sectionKey] ?? `sections.${sectionKey}`;
}
